package ReuseableLibraries;

public class ReuseableMethods {
    //create a void method to add two numbers
    public static void addTwoNumbers(int num1, int num2) {
        int result = num1 + num2;
        System.out.println("The result of the two numbers is " + result);
    }// end of add two numbers method

    //creqate a return integer method to subtract two numbers
    public static int subtractTwoNumbers(int a, int b){
        int result = a-b;
        System.out.println("The result of the two numbers for subtraction is " + result);

        return result;
    }//end of subtract return method
}// end of java class

